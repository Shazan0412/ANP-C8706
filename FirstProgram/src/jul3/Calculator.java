package jul3;

public class Calculator {
	    
	    public double calculateArea(double rad) {
	        return 3.14 * rad * rad;
	    }

	    
	    public double calculateArea(double length, double width) {
	        return length * width;
	    }

	    public double calculateArea(double base, double height, double width) {
	        
	        return (base * height) / 2;
	    }

	    public static void main(String[] args) {
	        Calculator calculator = new Calculator();

	    
	        double circleRadius = 5;
	        double circleArea = calculator.calculateArea(circleRadius);
	        System.out.println("Area of the circle: " + circleArea);

	        double rectangleLength = 4;
	        double rectangleWidth = 6;
	        double rectangleArea = calculator.calculateArea(rectangleLength, rectangleWidth);
	        System.out.println("Area of the rectangle: " + rectangleArea);

	        double triangleBase = 3;
	        double triangleHeight = 4;
	        double triangleWidth = 5; 
	        double triangleArea = calculator.calculateArea(triangleBase, triangleHeight, triangleWidth);
	        System.out.println("Area of the triangle: " + triangleArea);
	    }
	}

